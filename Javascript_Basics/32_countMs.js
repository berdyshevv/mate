/**
 * @param {string} text
 *
 * @returns {number}
 */
function countMs(text) {
    // write code here
    let word = text.toLowerCase ();
    let i = 0;
    for (let char of word) {
      if (char == 'm') {
        i +=  1;
  
      }
    } return i;
  }
 
